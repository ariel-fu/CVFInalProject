/**
 * This class models a skill in the routine.
 * 
 * @author Ariel
 *
 */
public class Skill {
  private String name;

  /**
   * Constructor to set the name 
   * @param name - name of the skill
   */
  public Skill(String name) {
    this.name = name;
  }
  
  public String getName() {
    return name;
  }
}
